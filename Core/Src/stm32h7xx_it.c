/* Private typedef -----------------------------------------------------------*/
/* USER CODE BEGIN TD */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart->Instance == USART2)
    {
        if (rx_index < sizeof(rx_buffer) - 1)
        {
            rx_buffer[rx_index++] = rx_byte;

            if (rx_byte == '\n')
            {
                rx_buffer[rx_index] = '\0';
                rx_ready = 1;
                rx_index = 0;
            }
        }
        else
        {
            rx_index = 0;
						clear_rx_buffer();
        }

        HAL_UART_Receive_IT(&huart2, &rx_byte, 1); // 继续接收
    }
}
